package com.tripointgames.problematic.entity;

/**
 * The state of the entity. This determines the texture of the entity, and
 * sometimes the behaviour of the entity as well.
 * 
 * @author Faizaan Datoo, Willie Hawley, and Alex Cevicelow
 */
public enum EntityState {

	/** The entity is standing still. */
	Standing,
	/** The entity is walking. */
	Walking,
	/** The entity is jumping. */
	Jumping;

}
